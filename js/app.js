function formatTime(date) {
  return date.toLocaleTimeString("es-MX", {
    hour: "2-digit",
    minute: "2-digit",
  });
}

function addLyricNotification(text, user) {
  const el = document.createElement("div");
  el.className = "notification";

  el.innerHTML = `
    <div class="notification-icon">
      <img src="./img/user.svg" alt="notification"
           style="width:100%; height:100%; object-fit:cover; border-radius:50%;">
    </div>
    <div class="notification-content">
      <div class="notification-title">${user} <span class="notification-time">${formatTime(new Date())}</span></div>
      <div class="notification-message">${text}</div>
    </div>
    
  `;

  notificationsContainer.prepend(el);

  const visible = notificationsContainer.querySelectorAll(
    ".notification:not(.removing)",
  );
  for (let i = visible.length - 1; i >= 4; i--) {
    visible[i].classList.add("removing");
    setTimeout(() => visible[i].remove(), 600);
  }
}

function syncLyrics(currentTime) {
  const currentLyrics = playlist[currentTrackIndex].lyrics;

  while (
    nextLyricIndex < currentLyrics.length &&
    currentTime >= currentLyrics[nextLyricIndex].time
  ) {
    const lyric = currentLyrics[nextLyricIndex];
    addLyricNotification(lyric.text, lyric.user);
    nextLyricIndex++;
  }
}

function resetLyricsFrom(currentTime) {
  const currentLyrics = playlist[currentTrackIndex].lyrics;

  let newIndex = 0;
  while (
    newIndex < currentLyrics.length &&
    currentTime >= currentLyrics[newIndex].time
  ) {
    newIndex++;
  }

  if (newIndex < nextLyricIndex) {
    notificationsContainer.innerHTML = "";
  }

  nextLyricIndex = newIndex;
}

const playSVG = `<svg width="14" height="14" viewBox="0 0 24 24" fill="currentColor">
  <path d="M8 5v14l11-7z"/>
</svg>`;

const pauseSVG = `<svg width="14" height="14" viewBox="0 0 24 24" fill="currentColor">
  <path d="M6 4h4v16H6V4zm8 0h4v16h-4V4z"/>
</svg>`;

function formatAudioTime(seconds) {
  const mins = Math.floor(seconds / 60);
  const secs = Math.floor(seconds % 60);
  return `${mins}:${secs.toString().padStart(2, "0")}`;
}

playBtn.addEventListener("click", () => {
  if (isPlaying) {
    audioPlayer.pause();
    playBtn.innerHTML = playSVG;
  } else {
    audioPlayer.play();
    playBtn.innerHTML = pauseSVG;
  }
  isPlaying = !isPlaying;
});

audioPlayer.addEventListener("loadedmetadata", () => {
  durationEl.textContent = formatAudioTime(audioPlayer.duration);
});

audioPlayer.addEventListener("timeupdate", () => {
  const percent = (audioPlayer.currentTime / audioPlayer.duration) * 100;
  progressFill.style.width = `${percent}%`;
  currentTimeEl.textContent = formatAudioTime(audioPlayer.currentTime);

  syncLyrics(audioPlayer.currentTime);
});

progressBar.addEventListener("click", (e) => {
  const rect = progressBar.getBoundingClientRect();
  const percent = (e.clientX - rect.left) / rect.width;
  audioPlayer.currentTime = percent * audioPlayer.duration;

  resetLyricsFrom(audioPlayer.currentTime);
});

audioPlayer.addEventListener("ended", () => {
  if (currentTrackIndex < playlist.length - 1) {
    changeTrack(currentTrackIndex + 1);
    audioPlayer.play();
    playBtn.innerHTML = pauseSVG;
    isPlaying = true;
  } else {
    playBtn.innerHTML = playSVG;
    isPlaying = false;
    progressFill.style.width = "0%";
    nextLyricIndex = 0;
    notificationsContainer.innerHTML = "";
  }
});

audioPlayer.addEventListener("seeked", () => {
  resetLyricsFrom(audioPlayer.currentTime);
});

const prevSVG = `<svg width="14" height="14" viewBox="0 0 24 24" fill="currentColor">
  <path d="M6 6h2v12H6zm3.5 6l8.5 6V6z"/>
</svg>`;

const nextSVG = `<svg width="14" height="14" viewBox="0 0 24 24" fill="currentColor">
  <path d="M6 18l8.5-6L6 6v12zM16 6v12h2V6h-2z"/>
</svg>`;

function changeTrack(index) {
  currentTrackIndex = index;
  const track = playlist[index];

  audioPlayer.src = track.src;
  audioPlayer.load();

  songTitleEl.textContent = track.title;
  songArtistEl.textContent = track.artist;
  albumArtEl.style.backgroundImage = `url('${track.albumArt}')`;

  progressFill.style.width = "0%";
  currentTimeEl.textContent = "0:00";
  durationEl.textContent = "0:00";

  nextLyricIndex = 0;
  notificationsContainer.innerHTML = "";

  updateSkipButtons();
}

function updateSkipButtons() {
  prevBtn.disabled = currentTrackIndex === 0;
  nextBtn.disabled = currentTrackIndex === playlist.length - 1;
}

prevBtn.addEventListener("click", () => {
  if (currentTrackIndex > 0) {
    const wasPlaying = isPlaying;
    changeTrack(currentTrackIndex - 1);

    if (wasPlaying) {
      audioPlayer.play();
      playBtn.innerHTML = pauseSVG;
      isPlaying = true;
    }
  }
});

nextBtn.addEventListener("click", () => {
  if (currentTrackIndex < playlist.length - 1) {
    const wasPlaying = isPlaying;
    changeTrack(currentTrackIndex + 1);

    if (wasPlaying) {
      audioPlayer.play();
      playBtn.innerHTML = pauseSVG;
      isPlaying = true;
    }
  }
});

volumeBtn.addEventListener("click", (e) => {
  e.stopPropagation();
  volumeSliderContainer.classList.toggle("show");
});

document.addEventListener("click", (e) => {
  if (!volumeSliderContainer.contains(e.target) && e.target !== volumeBtn) {
    volumeSliderContainer.classList.remove("show");
  }
});

volumeSlider.addEventListener("click", (e) => {
  const rect = volumeSlider.getBoundingClientRect();
  const percent = 1 - (e.clientY - rect.top) / rect.height;
  const volume = Math.max(0, Math.min(1, percent));

  audioPlayer.volume = volume;
  currentVolume = volume;
  volumeFill.style.height = `${volume * 100}%`;

  updateVolumeIcon(volume);
});

